/// Bessel of the second kind order one ( Y1 )
///
/// Max found ULP 0.500001
pub fn f_y1(x: f64) -> f64 {
    if x < 0. {
        return f64::NAN;
    }

    if !x.is_normal() {
        if x == 0. {
            return f64::NEG_INFINITY;
        }

        if x.is_nan() {
            return x + x;
        }

        if x.is_infinite() {
            if x.is_sign_negative() {
                return f64::NAN;
            }
            return 0.;
        }
    }

    let xb = x.to_bits();

    if xb <= 0x3ff6666666666666u64 {
        // 1.4
        return y1_near_zero_fast(x);
    }

    if xb <= 0x4049c00000000000u64 {
        // 51.5
        return y1_small_argument_path(x);
    }

    y1_asympt(x)
}

